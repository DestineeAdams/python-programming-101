# range(start, stop, step)

'''
start - Optional default is 0

stop - required
'''

# stop
for i in range(10):
    print(i)
    
print("------------------")
    
# start and stop
for i in range(2, 10):
    print(i)

    
print("------------------")
  
# start,  stop, step
for i in range(2, 10, 2):
    print(i)